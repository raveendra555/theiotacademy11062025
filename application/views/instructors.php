<script type="text/javascript">

  /*===========start instructors apply form submit=================*/

 $("#ApplyJobapp").submit(function(e){ 

    $("input[name='job_url_source_name']").val($(location).attr('href'));

    const formUrl ='<?=base_url()?>ApplyForJob/apply_job_submit_form';   
    const formData = new FormData(this);
    $.ajax({            

        type:'post',
        url : formUrl,
        processData: false,
        contentType: false,
        dataType:'json',
        //async:false,
        data: formData,
        beforeSend: function() {
            $("#enqform-overlay").show();
        },
        success: function (data) {
            if (data.message=="error") {
                alert(removeTags(data.response));
                $("#enqform-overlay").hide();
            }
            else{
                if (data.message=="success") {
                  $("#enqform-overlay").hide();
                  $("#ApplyJobapp")[0].reset();
                  $("#contact-success-msg").html(data.response);
                  $("#contact-success-msg").removeClass("hide");
                  $("#contact-success-msg").addClass("show");
                  $("#contact-success-msg").fadeIn();
                  $("#contact-success-msg").fadeOut(15000);
                }
                else{
                  $("#contact-error-msg").html(data.response);
                $("#contact-error-msg").removeClass("hide");
                $("#contact-error-msg").addClass("show");
                $("#contact-error-msg").fadeIn();
                $("#contact-error-msg").fadeOut(10000);
                }
            }
        } 
    });
});



/*=========== end apply instructors form submit=================*/
</script>

