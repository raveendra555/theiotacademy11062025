var Upcoming_event_Arr = [];
var Ongoing_event_Arr = [];
var Past_event_Arr = [];

var currentDate = new Date();
    currentDate.setHours(5);
    currentDate.setMinutes(30);
    currentDate.setSeconds(0);
    currentDate.setMilliseconds(0);
var count=0
for (var i = 0; i < eventsdata.length; i++) {
       count++;
       var startDate = new Date(eventsdata[i].start_date);
       var endDate = new Date(eventsdata[i].end_date);

    //For Upcoming Event
       if(startDate>currentDate) {
         Upcoming_event_Arr.push(eventsdata[i]);
       }
    //For Ongoing Event       
       else if (startDate<=currentDate && endDate>=currentDate) {
          Ongoing_event_Arr.push(eventsdata[i]);
       }
    // For Closed Event       
       else{
         Past_event_Arr.push(eventsdata[i]);
       }
}

//Upcoming Event Array ================
var upcoming_event=document.getElementById("upcoming_event");

if (Upcoming_event_Arr.length<=0) {
    upcoming_event.style.display="none";
}else{
    upcoming_event.style.display="block";
}

for (var i = 0; i < Upcoming_event_Arr.length; i++) {
   upcoming_event.appendChild(mainDiv( Upcoming_event_Arr[i]));
}

//Ongoing Event Array=================
var ongoing_events=document.getElementById("ongoing_events");

if (Ongoing_event_Arr.length<=0) {
   ongoing_events.style.display="none";
}else{
    ongoing_events.style.display="block";
}

for (var i = 0; i < Ongoing_event_Arr.length; i++) {
  ongoing_events.appendChild(mainDiv(Ongoing_event_Arr[i]));
}

//Closed Event Array==================
var closed_event=document.getElementById("closed_event");

if (Past_event_Arr.length<=0) {
    closed_event.style.display="none"; 
}else{
    closed_event.style.display="block";
}

for (var i = Past_event_Arr.length-1; i >=0 ; i--) {
   closed_event.appendChild(mainDiv(Past_event_Arr[i]));
}

