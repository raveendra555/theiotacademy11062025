    <script>
    function getTimeRemaining(endTime) {
        const total = Date.parse(endTime) - Date.parse(new Date());
        const seconds = Math.floor((total / 1000) % 60);
        const minutes = Math.floor((total / 1000 / 60) % 60);
        const hours = Math.floor((total / (1000 * 60 * 60)) % 24);
        const days = Math.floor(total / (1000 * 60 * 60 * 24));

        return {
            total,
            days,
            hours,
            minutes,
            seconds
        };
    }

    function getEndOfMonth() {
        const now = new Date();
        const year = now.getFullYear();
        const month = now.getMonth() + 1; // Months are 0-based, so add 1
        const endOfMonth = new Date(year, month, 0, 23, 59, 59); // Last day of the current month at 23:59:59
        return endOfMonth;
    }

    function padZero(number) {
        return number < 10 ? '0' + number : number;
    }

    function initializeClock() {
        const countdownElement = document.getElementById('countdown');
        const endTime = getEndOfMonth();

        function updateClock() {
            const t = getTimeRemaining(endTime);

            countdownElement.innerHTML = `
          <div><span>${padZero(t.days)}</span> Days</div>
          <div><span>${padZero(t.hours)}</span> Hrs</div>
          <div><span>${padZero(t.minutes)}</span> Mins</div>
          <div><span>${padZero(t.seconds)}</span> Sec</div>
        `;

            if (t.total <= 0) {
                clearInterval(timeInterval);
                countdownElement.innerHTML = "Time's up!";
            }
        }

        updateClock(); // run function once at first to avoid delay
        const timeInterval = setInterval(updateClock, 1000);
    }

    window.onload = initializeClock;
    </script>

