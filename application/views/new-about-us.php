    <script>
        document.addEventListener("DOMContentLoaded", function () {
        setTimeout(function () {
            function animateNumber(element, target) {
                let count = 0;
                const duration = 2000; 
                const startTime = performance.now();

                function updateNumber(currentTime) {
                    const elapsedTime = currentTime - startTime;
                    count = Math.min(target, Math.ceil((elapsedTime / duration) * target));
                    element.textContent = count + "+"; 

                    if (elapsedTime < duration) {
                        requestAnimationFrame(updateNumber); 
                    } else {
                        element.textContent = target + "+"; 
                    }
                }

                requestAnimationFrame(updateNumber); 
            }

            function startAnimation(progressBar) {
                const numElement = progressBar.querySelector('.num'); 
                
                if (numElement.classList.contains('no-animation')) {
                    numElement.textContent = "24*7"; 
                } else {
                    const targetValue = Number(numElement.getAttribute('data-target')); 
                    
                    if (!numElement.classList.contains('animated')) {
                        animateNumber(numElement, targetValue);
                        numElement.classList.add('animated'); 
                    }
                }

                progressBar.classList.add('show-progress');
            }

            const options = {
                threshold: 0.3 
            };

            const observer = new IntersectionObserver((entries, observer) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        startAnimation(entry.target); 
                        observer.unobserve(entry.target);
                    }
                });
            }, options);

            document.querySelectorAll('.progress-bar').forEach(progressBar => {
                observer.observe(progressBar); 

                progressBar.addEventListener('mouseover', () => {
                    startAnimation(progressBar);
                });
            });
        }, 2000); 
    });
